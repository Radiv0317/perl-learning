my $x = 10;
my $y = 20;

# Operator eq (equality)
if ($x eq $y) {
    print "$x sama dengan $y\n";
} else {
    print "$x tidak sama dengan $y\n";
}

# Operator ne (inequality)
if ($x ne $y) {
    print "$x tidak sama dengan $y\n";
} else {
    print "$x sama dengan $y\n";
}

# Operator lt (less than)
if ($x lt $y) {
    print "$x kurang dari $y\n";
} else {
    print "$x tidak kurang dari $y\n";
}

# Operator gt (greater than)
if ($x gt $y) {
    print "$x lebih besar dari $y\n";
} else {
    print "$x tidak lebih besar dari $y\n";
}

# Operator le (less than or equal)
if ($x le $y) {
    print "$x kurang dari atau sama dengan $y\n";
} else {
    print "$x tidak kurang dari atau sama dengan $y\n";
}

# Operator ge (greater than or equal)
if ($x ge $y) {
    print "$x lebih besar dari atau sama dengan $y\n";
} else {
    print "$x tidak lebih besar dari atau sama dengan $y\n";
}
