my $a = 10;
my $b = 20;

# Operator == (equality)
if ($a == $b) {
    print "$a sama dengan $b\n";
} else {
    print "$a tidak sama dengan $b\n";
}

# Operator != (inequality)
if ($a != $b) {
    print "$a tidak sama dengan $b\n";
} else {
    print "$a sama dengan $b\n";
}

# Operator < (less than)
if ($a < $b) {
    print "$a kurang dari $b\n";
} else {
    print "$a tidak kurang dari $b\n";
}

# Operator > (greater than)
if ($a > $b) {
    print "$a lebih besar dari $b\n";
} else {
    print "$a tidak lebih besar dari $b\n";
}

# Operator <= (less than or equal)
if ($a <= $b) {
    print "$a kurang dari atau sama dengan $b\n";
} else {
    print "$a tidak kurang dari atau sama dengan $b\n";
}

# Operator >= (greater than or equal)
if ($a >= $b) {
    print "$a lebih besar dari atau sama dengan $b\n";
} else {
    print "$a tidak lebih besar dari atau sama dengan $b\n";
}
