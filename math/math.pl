# Penjumlahan
my $a = 10;
my $b = 20;
my $sum = $a + $b;
print "Penjumlahan: $a + $b = $sum\n";

# Pengurangan
my $difference = $a - $b;
print "Pengurangan: $a - $b = $difference\n";

# Perkalian
my $product = $a * $b;
print "Perkalian: $a * $b = $product\n";

# Pembagian
my $division = $b / $a;
print "Pembagian: $b / $a = $division\n";

# Modulus (Sisa bagi)
my $modulus = $b % $a;
print "Modulus: $b % $a = $modulus\n";

# Operasi penugasan
my $c = 5;
$c += 2; # Sama dengan $c = $c + 2;
print "Operasi Penugasan: \$c += 2 menjadi $c\n";

# Operasi increment (penambahan 1)
my $d = 8;
$d++; # Sama dengan $d = $d + 1;
print "Operasi Increment: \$d++ menjadi $d\n";

# Operasi decrement (pengurangan 1)
my $e = 15;
$e--; # Sama dengan $e = $e - 1;
print "Operasi Decrement: \$e-- menjadi $e\n";