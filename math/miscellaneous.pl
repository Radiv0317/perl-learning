# Operator = (assignment)
my $x = 10;
my $y = $x;
print "Nilai y setelah assignment: $y\n";

# Operator . (string concatenation)
my $str1 = "Hello, ";
my $str2 = "World!";
my $result_str = $str1 . $str2;
print "Hasil concatenation: $result_str\n";

# Operator x (string multiplication)
my $str_repeat = "abc" x 3;
print "Hasil string multiplication: $str_repeat\n";

# Operator .. (range operator)
my @numbers = (1..5);
print "Range operator untuk angka: @numbers\n";

my @letters = ('A'..'E');
print "Range operator untuk huruf: @letters\n";
